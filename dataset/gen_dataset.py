    def smart_time_allocation(self, tasks: List[Tuple[str, int, int]], total_duration: int) -> List[Dict]:
        """智能时间分配算法"""
        result = []
        
        # 计算任务的最小时间需求和最大时间需求
        min_total = sum(task[1] for task in tasks)
        max_total = sum(task[2] for task in tasks)
        
        if total_duration < min_total:
            # 时间不够，压缩每个任务到最小时长
            current_day = 1
            for i, (task_name, min_days, max_days) in enumerate(tasks):
                task_id = f"task-{i+1:03d}"
                result.append({
                    "id": task_id,
                    "name": task_name,
                    "day": str(current_day) if min_days == 1 else f"{current_day}-{current_day + min_days - 1}"
                })
                current_day += min_days
        elif total_duration > max_total:
            # 时间充裕，可以延长某些任务或添加缓冲时间
            extra_days = total_duration - min_total
            current_day = 1
            
            for i, (task_name, min_days, max_days) in enumerate(tasks):
                # 按比例分配额外时间
                extra_for_task = min(extra_days // len(tasks), max_days - min_days)
                actual_days = min_days + extra_for_task
                
                task_id = f"task-{i+1:03d}"
                result.append({
                    "id": task_id,
                    "name": task_name,
                    "day": str(current_day) if actual_days == 1 else f"{current_day}-{current_day + actual_days - 1}"
                })
                current_day += actual_days
        else:
            # 时间刚好，智能分配
            remaining_days = total_duration
            current_day = 1
            
            for i, (task_name, min_days, max_days) in enumerate(tasks):
                remaining_tasks = len(tasks) - i
                avg_remaining = remaining_days // remaining_tasks if remaining_tasks > 0 else min_days
                actual_days = max(min_days, min(max_days, avg_remaining))
                
                task_id = f"task-{i+1:03d}"
                result.append({
                    "id": task_id,
                    "name": task_name,
                    "day": str(current_day) if actual_days == 1 else f"{current_day}-{current_day + actual_days - 1}"
                })
                
                current_day += actual_days
                remaining_days -= actual_days
                
        return result
    
